SUPPORTED_DASHBOARD_LOCALES = tuple(DASHBOARD_STRINGS)


def resolve_dashboard_locale(paths: ProjectPaths, requested_locale: str | None = None) -> str:
    locale = (requested_locale or _dashboard_locale_from_config(paths) or "en").strip()
    if locale not in DASHBOARD_STRINGS:
        supported = ", ".join(SUPPORTED_DASHBOARD_LOCALES)
        raise InvalidInputError(
            f"Invalid dashboard locale: {locale}. Supported locales: {supported}.",
            details={"locale": locale, "supported": list(SUPPORTED_DASHBOARD_LOCALES)},
        )
    return locale


def dashboard_strings(locale: str) -> dict[str, str]:
    return {**DASHBOARD_STRINGS["en"], **DASHBOARD_STRINGS.get(locale, {})}


def _dashboard_locale_from_config(paths: ProjectPaths) -> str | None:
    config_path = paths.root / "pcl.yaml"
    if not config_path.exists():
        return None
    try:
        lines = config_path.read_text(encoding="utf-8").splitlines()
    except OSError:
        return None
    dashboard = _simple_yaml_section(lines, "dashboard")
    locale = dashboard.get("locale", "").strip()
    return locale or None
